/**
 * scripts/split-routes-by-city.js
 *
 * Splits the monolithic routes.json (~13,808 routes) into 119 small JSON files,
 * one for each city, and writes them to src/data/routes-by-city/
 * so they are bundled as pre-compiled chunks.
 *
 * It also generates `src/lib/cityRouteShards.ts` containing static imports/lazy switch case.
 * It also pre-generates `src/data/popular-routes.json` to avoid parsing large JSONs on request-time.
 */

const fs = require('fs');
const path = require('path');

const routesPath = path.join(__dirname, '../src/data/routes.json');
const outDir = path.join(__dirname, '../src/data/routes-by-city');
const shardsTsPath = path.join(__dirname, '../src/lib/cityRouteShards.ts');
const popularRoutesPath = path.join(__dirname, '../src/data/popular-routes.json');

if (!fs.existsSync(routesPath)) {
  console.error('ERROR: src/data/routes.json not found. Run generate-routes.js first.');
  process.exit(1);
}

if (!fs.existsSync(outDir)) {
  fs.mkdirSync(outDir, { recursive: true });
}

const routes = JSON.parse(fs.readFileSync(routesPath, 'utf8'));

// Group by fromCity slug
const cityRoutes = {};
// Group by toCity slug
const cityToRoutes = {};

for (const route of routes) {
  // from grouping
  if (!cityRoutes[route.from]) {
    cityRoutes[route.from] = [];
  }
  cityRoutes[route.from].push(route);

  // to grouping
  if (!cityToRoutes[route.to]) {
    cityToRoutes[route.to] = [];
  }
  cityToRoutes[route.to].push(route);
}

// Write fromCity JSON files
const citySlugs = Object.keys(cityRoutes).sort();
for (const slug of citySlugs) {
  const filePath = path.join(outDir, `${slug}.json`);
  fs.writeFileSync(filePath, JSON.stringify(cityRoutes[slug], null, 2));
}

// Write toCity JSON files
const outToDir = path.join(__dirname, '../src/data/routes-to-city');
if (!fs.existsSync(outToDir)) {
  fs.mkdirSync(outToDir, { recursive: true });
}
const cityToSlugs = Object.keys(cityToRoutes).sort();
for (const slug of cityToSlugs) {
  const filePath = path.join(outToDir, `${slug}.json`);
  fs.writeFileSync(filePath, JSON.stringify(cityToRoutes[slug], null, 2));
}

console.log(`✓ Wrote ${citySlugs.length} from-city and ${cityToSlugs.length} to-city specific route JSON files.`);

// Pre-calculate popular routes
const hubSlugs = ['kolkata', 'ranchi', 'bhubaneswar'];
const popularDestinations = new Set([
  'darjeeling', 'puri', 'digha', 'deoghar', 'konark', 'ranchi',
  'bhubaneswar', 'kolkata', 'jamshedpur', 'mandarmani', 'gangasagar',
  'mayapur', 'siliguri', 'durgapur', 'asansol', 'dhanbad', 'bokaro',
]);

const popularRoutes = routes
  .filter(r => hubSlugs.includes(r.from) && popularDestinations.has(r.to))
  .slice(0, 50);

fs.writeFileSync(popularRoutesPath, JSON.stringify(popularRoutes, null, 2));
console.log(`✓ Generated ${popularRoutesPath} with ${popularRoutes.length} popular routes.`);

// Generate the TypeScript file src/lib/cityRouteShards.ts
let tsContent = `/**
 * src/lib/cityRouteShards.ts
 *
 * Auto-generated by scripts/split-routes-by-city.js
 * Do not edit this file manually.
 */

import { Route } from './data';

export async function getCityRouteShard(citySlug: string): Promise<Route[]> {
  switch (citySlug) {
`;

for (const slug of citySlugs) {
  tsContent += `    case '${slug}':\n`;
  tsContent += `      return (await import('@/data/routes-by-city/${slug}.json')).default as unknown as Route[];\n`;
}

tsContent += `    default:\n`;
tsContent += `      return [];\n`;
tsContent += `  }\n`;
tsContent += `}\n`;

fs.writeFileSync(shardsTsPath, tsContent);
console.log(`✓ Generated ${shardsTsPath}`);

// Generate the TypeScript file src/lib/cityRouteToShards.ts
const shardsToTsPath = path.join(__dirname, '../src/lib/cityRouteToShards.ts');
let tsToContent = `/**
 * src/lib/cityRouteToShards.ts
 *
 * Auto-generated by scripts/split-routes-by-city.js
 * Do not edit this file manually.
 */

import { Route } from './data';

export async function getCityRouteToShard(citySlug: string): Promise<Route[]> {
  switch (citySlug) {
`;

for (const slug of cityToSlugs) {
  tsToContent += `    case '${slug}':\n`;
  tsToContent += `      return (await import('@/data/routes-to-city/${slug}.json')).default as unknown as Route[];\n`;
}

tsToContent += `    default:\n`;
tsToContent += `      return [];\n`;
tsToContent += `  }\n`;
tsToContent += `}\n`;

fs.writeFileSync(shardsToTsPath, tsToContent);
console.log(`✓ Generated ${shardsToTsPath}`);
